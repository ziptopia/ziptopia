        <h1>인테리어 하자가 생길 수 밖에 없는 이유</h1>
        <p>많은 건축 인테리어 업체 대표님들께서 인테리어 하자는 <br>
          무조건, 99% 생길 수 밖에 없다고 얘기를 합니다. <br><br>
          왜 그런걸까요?
        </p>
      </div>
      <div class="content">
        <p>
          안녕하세요. 오늘은〈 인테리어 하자는 왜 생기는 걸까? 〉에 대해 이야기해보려고 합니다.
          많은 건축 인테리어 업체 대표님들께서 인테리어 하자는 무조건, 99% 생길 수 밖에 없다고 얘기를 합니다.<br><br>
          왜 그런걸까요?<br><br>
          그 이유를 크게 2가지로 얘기해볼 수 있습니다. 
        </p>
        <p>
          1. 소비자가 생각하는 인테리어 예상도와 인테리어 시공완료 후 실물 사이의 차이 <br>
          2. 시공과정에서의 물리적 하자
        </p>
        <p>
          소비자는 본인이 원하는 사진을 가지고 인테리어 업체와 의논하여 인테리어를 최종 확정을 짓게 됩니다. 하지만 이런 논의 과정에서  <br>
          소비자와 인테리어 업체와 의사소통이 충분히 되지 않으면 소비자와 인테리어업체간의 인테리어완성의 차이가 생깁니다. 소비자는 본인이
          생각한 것과 실물이 차이가 나면 인테리어가 잘못 됐다고 생각 할 수 있습니다.
        </p>
        <div>
          <img src="/images/tip/tip01_01.png" alt="팁이미지01">
        </div>
        <p>
          아무리 사전에 많은 시간을 들여 논의하더라도 인테리어업체는 소비자 본인이 아니므로 결과물의 차이가 생기는 것은 당연합니다. <br>
          그 차이를 줄이기 위해 3D 이미지를 꼭 요청해 소비자 본인이 생각한 인테리어이미지와 일치하는지 확인하세요. 머릿속으로 상상하는
          것과 직접 이미지를 통해 확인하는 것과는 차이가 크기 때문에 거실과 욕실 등 본인이 중요하다고 생각되는 공간이라면 3D 이미지를
          뽑아보시길 추천합니다.<br><br><br>

          진짜 문제는 물리적 하자부분인데요. 이건 소비자와 업체와의 커뮤니케이션의 문제가 아니라 기술적인 문제라서 소비자가 어떻게 할 수
          없는 부분입니다. <br><br><br>

          가장 많은 문제가 생기는 것 중 하나인 타일을 예를 들어볼까요?<br>
          화장실이나 부엌의 벽면 타일의 경우 조금 튀어나와도 큰 불편함이 없습니다. 하지만 바닥 타일 같은 경우 어떤 소비자는 1mm에 어떤
          소비자는 2mm가 들떠 있을때 불편함을 느낍니다. 발에 걸리는 되도 불편함을 느끼는 정도가 소비자마다 다르다는 거죠. <br>
          어쨌든 이렇게 타일이 들뜨면 굉장히 불편합니다. 슬리퍼든 발가락이든 걸리게 되니까요. <br><br><br>

          이 부분이 매끄럽게 시공이 되지 않았기 때문에 소비자는 시공 업체에게 하자보수를 요청합니다. 이때 업체가 이 정도는 하자가 아니다라고
          하자 보수를 거부하게 되면 이때부터 분쟁이 생기기 시작하는 거죠. 만약 계약서에 하자에 대해 〈 ~부터 ~까지는 하자로 인정 〉이라고
          명시되어 있다면다행이겠지만 하자에 대한 명확한 기준이 없다면 애매해지는 부분이죠.  <br>
          우리 소비자 입장에서는 들뜬 타일이 발에 걸리니까 하자라고 생각해 보수 해달라고 하는 것인데 업체 입장에서는 타일이 깨진 것도
          니기 때문에 거절할 가능성이 높습니다. 들뜬 1장을 보수하기 위해서는 주변의 타일까지 다시 다 들어내고 다시 시공해야 하는 상황이
          발생하기 때문이죠. 그렇게 되면 업체 입장에서는 비용이 발생하겠죠. 업체입장에서는 손해가 발생하는 부분이기 때문에 해주려고 하지
          않을 겁니다.<br><br><br>

          타일의 경우 잘 살펴보시면 중간이 둥그렇게 떠있는 타일을 발견하실 수 있을 거예요. 도자기 처럼 굽기 때문이라고 합니다. 그리고 간혹
          타일이 휘어져있는 경우가 있어요. 타일은 일정 mm이상 휘어지면 불량이므로 미리 골라내고 사용하면 좋겠지만 소비자들이 일일이 
          판별 할 수 없고 해당 타일을 일일이 골라내기에는 추가 자재구매 + 인력 비용이 들어가기 때문에 업체에서는 그냥 사용하는 경우들이
          있다고 합니다.
        </p>
        <div>
          <img src="/images/tip/tip01_02.png" alt="팁이미지02">
        </div>
        <p>
          이런 경우를 예방하기 위해서는 처음부터 1mm 또는 2mm 이렇게 미리 하자에 대한 기준을 정해 놓는 것이 좋습니다. 그럴 경우 견적에서
          비용이 추가가 되겠죠. 하지만 하자에 대한 스트레스보다는 낫지 않을까 생각합니다.<br><br><br>

          목재의 경우, 수분 함수율이 높은 목재가 있어요. 이런 목재는 인테리어 후에 마르면서 뒤틀림, 갈라짐과 같은 문제가 발생할 수 있습니다.
          이 경우 잘못하면 벽지가 갈라지는 하자가 생길 수 있습니다. 또 정상적인 목재라고 해도 사람들의 많이 다니는 곳 같은 경우에는 목재가
          내려앉거나, 뒤틀려서 디딜 때 마다 삐그덕 거리는 소리가 나는 경우를 볼 수 있습니다. 이것도 사실 하자라면 하자라고 할 수 있습니다.<br><br><br>

          이 경우에는 누구의 잘못일까요? 누구 잘못이든 입주 후 1년 내에 이런 문제가 생가면 하자보수라도 얘기할 수 있는데 잘 사용하다가 2년
          후에 이런 문제가 발생한다면 소비자의 비용으로 보수를 해야합니다. 하자보수 기간이 지났기 때문이죠.<br><br><br>

          초기 공사의 하자가 아닌 사용하면서 발생하는 문제에 대해서 우리 소비자들은 어떻게 방지해야 할까요? 사용하면서 발생하는 문제에
          대해서는 방지를 할 수 없습니다. 사용함에 따라 마모가 되는 부분이기 때문에 문제가 생긴다면 그때 그때 보완하는 방법밖에 없다고
          합니다.<br><br><br>

        </p>
        <h4>추가정보</h4>
        <p>
          타일이나 벽돌의 경우 롯트 번호가 같아도 색상이 미세하게 차이가 납니다. <br>
          그 이유는 고온에 굽기 때문인데 온도 차이와 굽는 시간 차이 등 미세한 차이로 인해 색이 같을 수가 없습니다. 같은 공장에서 나온
          것인데도 말이죠. 
        </p>



