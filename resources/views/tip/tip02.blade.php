        <p>
          계약이행이 완료되어 검수가 끝난 후, 하자보증기간 중에 발생하는 하자에 대해여 채무자(시공업체)가 하자 보수요청을 받고도 이를
          이행하지 아니함으로써 채권자(소비자)가 입게되는 손해를 배상하는 보험을 말합니다. 하자이행보증증권에 가입이 되어있다면 이행되
          지 않아 발생하는 소비자의 손해를 줄일 수 있겠죠? <br><br>

