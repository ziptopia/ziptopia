        <p>
          인테리어 업체를 선정하기 전 여러곳에 인테리어 견적을 넣어보신 경험 많으실 겁니다. 하지만 견적을 받아봐도 어떤 견적이 적당한견적인
          지 잘 모르실 거예요. <br><br>

          정확한 비교견적을 받기 위해서는 비교할 대상이 고정되어 있어야 합니다.<br>
          비교할 대상이 고정되어 있지 않으면 3곳이든 10곳이든 견적을 받아도 비교가 되지 않겠죠? 비교를 위해서 다음 3가지 기준만 정해지면
          됩니다.
        </p>
        <h4 class="box01">3가지 기준</h4>
        <div>
          <figure>
            <img src="/images/tip/tip05/tip05_01.png" alt="팁이미지01">
            <figcaption>평수</figcaption>
          </figure>
          <figure>
            <img src="/images/tip/tip05/tip05_02.png" alt="팁이미지02">
            <figcaption>자재</figcaption>
          </figure>
          <figure>
            <img src="/images/tip/tip05/tip05_03.png" alt="팁이미지03">
            <figcaption>구조변경</figcaption>
          </figure>
        </div>
        <p>
          업체를 선정하셨다면 그 인테리어 계약서를 작성해야겠죠? <br>
          계약서는  최대한 업체와 협의된 내용을 특약으로 넣으시면 되요. 기본 계약서 양식은 정부에서 제공한 인테리어 표준 계약서를 기본으로 
          사용하면 되구요. 거기에 설계도서와 사업자등록증사본, 계약 당사자 주민등록증 사본, 보증보험증권을 반드시 첨부하도록 합니다.
        </p>
        <h4>◆ 평수</h4>
        <p>
          기본적으로 공간의 크기와는 상관없이 보통 시공완료하는 기간은 비슷합니다. 예를 들어 30평공사가 4주인데 50평은 7주가 걸리지는 <br>
          않아요. 평수가 커지면 시공기간이 길어지는 것이 아니라 시공인력이 더 투입되기 때문에 시공기간은 거의 비슷합니다.<br>
          만약 평수가 커서 공사기간이 길어지니까 가격이 비싸다, 이런 얘기하는 업체와는 거래하지 않는게 좋습니다.
        </p>
        <h4>◆ 자재</h4>
        <p>
          비교견적은 소비자 여러분이 원하는 디자인과, 자재를 결정하고 그 자료를 인테리어 업체에게 요청을 하셔야 확실하게 비교가 가능합니다. <br>
          업체들에게 서로 다른 사진을 보여준 후 견적을 달라고 요청을 하시면 비교자체가 불가능하겠죠?<br>
        </p>
        <h4>◆ 구조변경</h4>
        <p>
          구조변경이란 베란다 확장, 화장실 구조 변경, 방 트기 등등<br>
          공간의 구조를 바꾸는 모든 행위인데 이렇게 구조가 변경되는 경우 법적 허가사항일 경우도 있으니 반드시 확인이 필요합니다.<br>
          또한 공사가 커지는 주요 요인이 되기도 합니다.<br><br>

          정리하자면 동일한 사진, 동일한 자재, 동일한 평수, 동일한 구조변경 내용을 업체에게 전달해줘야 비교가 가능합니다. 그렇게 되면 업체
          마다 동일한 조건에서 견적을 받게 되니까 어디가 비싸고, 어디가 싼지 확실하게 비교가 가능하겠죠?<br><br>

          이때 3곳 견적을 의뢰했는데 한곳이 지나치게 싸다? 이곳은 무조건 빼는 것이 좋습니다. 공사를 하다가 잠적하거나 공사비용을 추가로 더
          지불해 달라고 하는 업체인 경우가 대부분입니다.<br>
          (이 부분은 하단에 ‘인테리어 평당원간 계산하는 A to Z ’ 영상 바로가기를 추가 해드릴테니 영상으로 봐주세요.)<br><br>

          어떤 업체라도 너무 큰 견적 차이는 날 수 없어요. 비싸면 모를까 싸질 수는 없습니다. <br><br>

          자, 이렇게 해서 견적을 받으셨다면 그 견적서에 모르는 내용들이 있을 거예요. 업체마다 조금씩 견적내는 용어가 다르고 견적에 들어가는
          내용들이 다를겁니다. 그럴 때는 업체에게 꼭 물어보세요. A업체에게 받은 내용으로 B업체에게 묻고, B업체의 견적서 중 모르는 내용은 
          C업체에게 물어보세요. 그렇다면 업체에서 비교해 설명을 해주실 겁니다.<br><br>

          만약 이러한 과정 중에서 견적 설명을 귀찮아하거나, 상세견적서를 제공해주지 않는 업체가 있다면 그런 업체들은 빼는 것이 좋습니다.
          인테리어 업체들도 서로 경쟁하는 부분이기 때문에 소비자의 마음을 사기 위해 서비스라던가, 시공에 관한 이야기들이라던가 그런
          부분들을 서로 더 자세하게 설명을 해주실 거예요. <br><br>

          이렇게 많은 견적으로 설명을 들으신 후 마음에 드시는 업체를 선택하셨다면 이제 계약서를 작성하시면 됩니다. <br><br>
        </p>
        
