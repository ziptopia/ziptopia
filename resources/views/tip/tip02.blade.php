        <h4>◆ 설계도</h4>
        <p>
          공사 전 세부적인 조건들을 반영해서 실제로 시공시에 발생할 수 있는 낭비요소를 줄일 수 있기 때문에 설계도는 꼭 필요한 사항입니다. 
          혹 인테리어 공사를 의뢰했는데 도면 제작을 미룬다거나, 도면의 결과물이 만족스럽지 않다면 그 회사와는 공사를 함께 하지 않는 것이
          좋습니다. 인테리어 회사가 ‘설계와 시공’을 병행해서 수행하고 있어서 설계를 잘 못한다는 것은 시공의 품질을 신뢰하기 어렵습니다. 
          도면의 종류에는 평면도/천장도/입면도/단면도/상세도 등으로 시공상 중요한 부분을 상세하게 표현합니다. 보통 인테리어에서는
          평면도/천장도/입면도 정도가 가장 많이 사용되기 때문에 세 가지 정도만 알고 계시면 됩니다.
        </p>
        <h4>◆ 표준시방서</h4>
        <p>
          시방서는 설계도면으로 나타낼 수 없는 사항을 문서로 쓴거라고 생각하면 됩니다. 인테리어를 할 때 도면상으로 나타낼 수 없는 부분이
          존재하게 됩니다. 이러한 세부 사항들을 도면이 아닌 다른곳에 명시한 문서라고 생각하시면 되요. 일반적으로는 시방서에 자재의
          재질이나, 품질, 시공방법 등을 표시하게 되어있고 설계도면과 함께 빠질 수 없는 중요한 부분을 구성하는 문서입니다.
        </p>
        <h4>◆  자재명세서</h4>
        <p>
          자재명세서는 각 자재의 회사명 제품명, 롯트번호, 수량, 가격이 표시되어야 하구요. 나중에 자재명세서대로 자재가 사용됐는지 확인하는
          기준이 됩니다. 그리고 자재명세서와 다르게 자재를 사용하는 비양심적인 업체들에게 법적 조치하는 증거가 될 수 있으니 꼭 받아두셔야
          합니다.
        </p>
